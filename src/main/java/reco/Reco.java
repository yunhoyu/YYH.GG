package reco;

public class Reco {
	private int bbsID;
	private int like_No;
	private int hate_No;
	
	public int getBbsID() {
		return bbsID;
	}
	public void setBbsID(int bbsID) {
		this.bbsID = bbsID;
	}
	public int getLike_No() {
		return like_No;
	}
	public void setLike_No(int like) {
		this.like_No = like;
	}
	public int getHate_No() {
		return hate_No;
	}
	public void setHate_No(int hate) {
		this.hate_No = hate;
	}
}
